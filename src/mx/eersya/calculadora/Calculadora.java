    private void operacionBasicaBoton(javax.swing.JButton jButton) {
        
        if(estado != REALIZANDO_OPERACION && estado != EN_PUNTO && estado != SIGNO) {
            if((estado == VACIO)&& (jButton.getLabel().equals(SUMAR) || jButton.getLabel().equals(RESTAR))) {
                insertarCaracter(jButton);
                estado = SIGNO;
            } else {
                double actualNum = (Datos.isInteger(pantalla.getText().substring(longitud, pantalla.getText().length()))) 
                            ? Double.parseDouble(pantalla.getText().substring(longitud, pantalla.getText().length())) : 0;
                if(presionado == NO_PRESIONADO) {
                    resultado = actualNum;
                    actualNum = 0;
                }
                switch(operacion) {
                    case SUMAR:
                        resultado += actualNum;
                        break;
                    
                    case RESTAR:
                        resultado -= actualNum;
                        break;
                        
                    case MULTIPLICAR:
                        if(!(actualNum == 0 && presionado == NO_PRESIONADO)) {
                            resultado *= actualNum;
                            pantalla.setText(String.valueOf(resultado));
                        }
                        break;
                        
                    case DIVIDIR:
                        
                        if(presionado != NO_PRESIONADO) {
                            resultado /= actualNum;
                            if(Double.isInfinite(resultado) || Double.isNaN(resultado)) {
                                JOptionPane.showMessageDialog(rootPane, "NaN");
                                resultado = 0;
                            }
                            pantalla.setText(String.valueOf(resultado));
                        }
                            
                        break;    
                }
                operacion = jButton.getLabel();
                insertarCaracter(jButton);
                longitud = pantalla.getText().length();
                estado = REALIZANDO_OPERACION;
                if(jButton.getLabel().equals(IGUAL)) {
                    pantalla.setText(String.valueOf(resultado));
                }
                presionado = PRESIONADO;
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Ocupado y/o vacio.");
        }
        
        System.out.printf("resultado: %f ,numActual: %s", resultado, operacion);
    }
    
    private void insertarCaracter(javax.swing.JButton jButton) {
        if(Datos.isInteger(jButton.getLabel())) {
            estado = NUMERO;
        } else if(PUNTO.equals(jButton.getLabel())) {
            estado = EN_PUNTO;
        }
        pantalla.setText(pantalla.getText() + jButton.getLabel());
    }
