    private void cosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cosActionPerformed
        if(estado != VACIO && estado != REALIZANDO_OPERACION) {
            if(Double.isNaN(Math.cos(resultado)) || Double.isInfinite(Math.cos(resultado))) {
                JOptionPane.showMessageDialog(rootPane, "NaN");
                pantalla.setText(null);
            } else
                pantalla.setText(String.valueOf(Math.cos(resultado)));
        } else {
            JOptionPane.showMessageDialog(rootPane, "Ocupado y/o vacio.");
        }
    }//GEN-LAST:event_cosActionPerformed

